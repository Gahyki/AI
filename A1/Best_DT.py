from __future__ import division
from codecs import open


def read_documents(doc_file):
    docs = []
    labels = []
    with open(doc_file,encoding='utf-8') as f:
        for line in f:
            words= line.strip().split()
            docs.append(words[3:])
            labels.append(words[1])

    return docs,labels



